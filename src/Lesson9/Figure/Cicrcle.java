package Lesson9.Figure;

public class Cicrcle extends Figure {
    double radius;

    public Cicrcle(double radius) {
        this.radius = radius;
    }

    @Override
    public double calculateArea() {
        return Math.PI * radius * radius;
    }

    @Override
    public double calculatePerimeter() {
        return 2 * Math.PI * radius;
    }
}
